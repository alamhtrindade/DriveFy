    public void addTabela(Produto prod, int qtd, double valor) {
        DefaultTableModel tabela = (DefaultTableModel) tableVendas.getModel();

        if (!buscaTabelaAntesADD(prod, qtd, valor)) {
            Object[] row = {prod.getId(), prod.getTipo(), prod.getValor(), qtd, valor};
            tabela.addRow(row);
            double venda = Double.parseDouble(valorVenda.getText());
            venda = +valor;

            buscaatualizaTotal();
        }
    }

    public void limpaTabela() {
        DefaultTableModel tabela = (DefaultTableModel) tableVendas.getModel();
        while (tabela.getRowCount() > 0) {
            tabela.removeRow(0);
        }

    }

