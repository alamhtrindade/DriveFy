    public void carregarDadosComboNome() {

        ComboNome.removeAllItems();

        String cpf = this.inputCpf.getText().toLowerCase();

        if (cpf.isBlank() || cpf == null || cpf.isEmpty()) {
            for (Cliente p : clientes) {

                ComboNome.addItem(p.getNome() + "=>" + p.getCpf());

            }
        } else {
            for (Cliente p : clientes) {
                if (p.getCpf().toLowerCase().contains(cpf)) {

                    ComboNome.addItem(p.getNome() + "=>" + p.getCpf());

                }
            }
        }

    }

