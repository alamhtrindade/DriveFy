    public boolean buscaTabelaAntesADD(Produto prod, int qtd, double valor) {
        DefaultTableModel tabela = (DefaultTableModel) tableVendas.getModel();
        boolean atualiza = false;

        for (int i = 0; i < tabela.getRowCount(); i++) {
            if (tabela.getValueAt(i, 1).toString().equals(prod.getTipo())) {
                int qtd_atual = Integer.parseInt(tabela.getValueAt(i, 3).toString());
                qtd_atual = qtd + qtd_atual;
                if (qtd_atual > prod.getQuantidade()) {
                    JOptionPane.showMessageDialog(rootPane, "Não é possivel adicionar a quantidade estabelecida uma vez que ja ultrapassa a quantidade maxima!");
                } else {
                    double valor_atual = Double.parseDouble(tabela.getValueAt(i, 4).toString());
                    valor_atual = valor_atual + valor;
                    tabela.setValueAt(qtd_atual, i, 3);
                    tabela.setValueAt(valor_atual, i, 4);
                    buscaatualizaTotal();
                }

                atualiza = true;
                break;
            }

        }
        return atualiza;
    }

    public void buscaatualizaTotal() {
        DefaultTableModel tabela = (DefaultTableModel) tableVendas.getModel();
        double valor_atual = 0;
        for (int i = 0; i < tabela.getRowCount(); i++) {
            valor_atual += Double.parseDouble(tabela.getValueAt(i, 4).toString());
        }

        valorVenda.setText(Double.toString(valor_atual));
    }

    public void carregarProduto() {
        inputProduto.removeAllItems();

        for (Produto p : produtos) {
            if (p.getQuantidade() > 0) {
                inputProduto.addItem(p.getTipo());
            }

        }

    }

