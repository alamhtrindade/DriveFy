    private void bAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAdicionarActionPerformed
        // TODO add your handling code here:
        Produto prod = null;
        int qtd = Integer.parseInt(this.qtd.getValue().toString());
        double valor;
        String comboProd = (String) inputProduto.getSelectedItem();

        for (Produto p : produtos) {
            if (p.getTipo().equals(comboProd)) {
                prod = p;
            }
        }

        if (qtd > prod.getQuantidade()) {
            JOptionPane.showMessageDialog(rootPane, "A quantidade para venda ultrapassa o estoque atual!");
        } else if (qtd == 0) {
            JOptionPane.showMessageDialog(rootPane, "Não há quantidade para vender!");
        } else {
            valor = qtd * prod.getValor();
            addTabela(prod, qtd, valor);
        }


    }//GEN-LAST:event_bAdicionarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        String CPFCliente = "";
        String Id_produto = "";
        String Nome_produto = "";
        String qtd = "";
        String preco = "";
        String totalVenda = "";
        String Data = "";
        ArrayList<Produto> alterados = new ArrayList<>();

        boolean gravar = false;

        if (Cadastrado.isSelected()) {
            if (ComboNome.getSelectedItem() == null) {
                CPFCliente = "Nao Cadastrado";
            } else {
                CPFCliente = ComboNome.getSelectedItem().toString();
            }

        } else {
            CPFCliente = "Nao Cadastrado";
        }

        DefaultTableModel tabela = (DefaultTableModel) tableVendas.getModel();
        if (tabela.getRowCount() > 0) {
            for (int i = 0; i < tabela.getRowCount(); i++) {

                for (Produto a : produtos) {
                    int b = Integer.parseInt(tabela.getValueAt(i, 0).toString());
                    if (a.getId() == b) {
                        a.setQuantidade(a.getQuantidade()-(Integer.parseInt(tabela.getValueAt(i, 3).toString())));
                        alterados.add(a);
                    }
                }

                Id_produto += "[" + tabela.getValueAt(i, 0).toString() + "]";
                Nome_produto += "[" + tabela.getValueAt(i, 1).toString() + "]";
                preco += "[" + tabela.getValueAt(i, 2).toString() + "]";
                qtd += "[" + tabela.getValueAt(i, 3).toString() + "]";

            }
            gravar = true;
        }

        if (valorVenda.getText().equals("") || valorVenda.getText().equals("0.00") || valorVenda.getText().equals("0.0") || valorVenda.getText().equals("0")) {
            gravar = false;
        } else {
            totalVenda = valorVenda.getText();
        }

        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
        Data = dtf.format(LocalDateTime.now());

        if (gravar) {
            Venda venda = new Venda(CPFCliente, Id_produto, Nome_produto, preco, qtd, totalVenda, Data);

            b.cadastrarVenda(venda,alterados);
            JOptionPane.showMessageDialog(rootPane, "Venda concluida com sucesso!");
            this.dispose();
        } else {

            JOptionPane.showMessageDialog(rootPane, "Não é possivel registrar a venda, verifique se os campos estão corretos!");

        }


    }//GEN-LAST:event_btnSalvarActionPerformed

